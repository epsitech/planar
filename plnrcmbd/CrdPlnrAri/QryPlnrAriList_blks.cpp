/******************************************************************************
 class QryPlnrAriList::VecVOrd
 ******************************************************************************/

uint QryPlnrAriList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "i") return I;
	else if (s == "j") return J;
	else if (s == "k") return K;
	else if (s == "l") return L;
	else if (s == "m") return M;
	else if (s == "n") return N;
	else if (s == "arr") return ARR;
	else if (s == "dsn") return DSN;

	return(0);
};

string QryPlnrAriList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == I) return("i");
	else if (ix == J) return("j");
	else if (ix == K) return("k");
	else if (ix == L) return("l");
	else if (ix == M) return("m");
	else if (ix == N) return("n");
	else if (ix == ARR) return("arr");
	else if (ix == DSN) return("dsn");

	return("");
};

void QryPlnrAriList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=8;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

