/******************************************************************************
 class QryPlnrRtcList::VecVOrd
 ******************************************************************************/

uint QryPlnrRtcList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "srf") return SRF;
	else if (s == "hkt") return HKT;
	else if (s == "hku") return HKU;
	else if (s == "dsn") return DSN;
	else if (s == "lyr") return LYR;

	return(0);
};

string QryPlnrRtcList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == SRF) return("srf");
	else if (ix == HKT) return("hkt");
	else if (ix == HKU) return("hku");
	else if (ix == DSN) return("dsn");
	else if (ix == LYR) return("lyr");

	return("");
};

void QryPlnrRtcList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=5;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

