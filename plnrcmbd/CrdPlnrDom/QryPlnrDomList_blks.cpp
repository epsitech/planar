/******************************************************************************
 class QryPlnrDomList::VecVOrd
 ******************************************************************************/

uint QryPlnrDomList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "srf") return SRF;
	else if (s == "typ") return TYP;
	else if (s == "dsn") return DSN;

	return(0);
};

string QryPlnrDomList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == SRF) return("srf");
	else if (ix == TYP) return("typ");
	else if (ix == DSN) return("dsn");

	return("");
};

void QryPlnrDomList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=3;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

