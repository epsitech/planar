/******************************************************************************
 class QryPlnrUsrList::VecVOrd
 ******************************************************************************/

uint QryPlnrUsrList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "prs") return PRS;
	else if (s == "srf") return SRF;
	else if (s == "usg") return USG;

	return(0);
};

string QryPlnrUsrList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == PRS) return("prs");
	else if (ix == SRF) return("srf");
	else if (ix == USG) return("usg");

	return("");
};

void QryPlnrUsrList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=3;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

