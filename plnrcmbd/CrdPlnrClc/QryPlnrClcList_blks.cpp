/******************************************************************************
 class QryPlnrClcList::VecVOrd
 ******************************************************************************/

uint QryPlnrClcList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s == "tit") return TIT;
	else if (s == "prj") return PRJ;
	else if (s == "dim") return DIM;
	else if (s == "tpl") return TPL;
	else if (s == "sup") return SUP;
	else if (s == "dsn") return DSN;

	return(0);
};

string QryPlnrClcList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == TIT) return("tit");
	else if (ix == PRJ) return("prj");
	else if (ix == DIM) return("dim");
	else if (ix == TPL) return("tpl");
	else if (ix == SUP) return("sup");
	else if (ix == DSN) return("dsn");

	return("");
};

void QryPlnrClcList::VecVOrd::fillFeed(
			Feed& feed
		) {
	feed.clear();

	for (unsigned int i=1;i<=6;i++) feed.appendIxSrefTitles(i, getSref(i), getSref(i));
};

