/******************************************************************************
 class PnlPlnrPrj1NCalc::VecVDo
 ******************************************************************************/

uint PnlPlnrPrj1NCalc::VecVDo::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s.compare("butdeleteclick") == 0) return BUTDELETECLICK;
	else if (s.compare("butrefreshclick") == 0) return BUTREFRESHCLICK;

	return(0);
};

string PnlPlnrPrj1NCalc::VecVDo::getSref(
			const uint ix
		) {
	if (ix == BUTDELETECLICK) return("ButDeleteClick");
	else if (ix == BUTREFRESHCLICK) return("ButRefreshClick");

	return("");
};

