/******************************************************************************
 class QryPlnrAriList::VecVOrd
 ******************************************************************************/

uint QryPlnrAriList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s.compare("i") == 0) return I;
	else if (s.compare("j") == 0) return J;
	else if (s.compare("k") == 0) return K;
	else if (s.compare("l") == 0) return L;
	else if (s.compare("m") == 0) return M;
	else if (s.compare("n") == 0) return N;
	else if (s.compare("arr") == 0) return ARR;
	else if (s.compare("dsn") == 0) return DSN;

	return(0);
};

string QryPlnrAriList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == I) return("i");
	else if (ix == J) return("j");
	else if (ix == K) return("k");
	else if (ix == L) return("l");
	else if (ix == M) return("m");
	else if (ix == N) return("n");
	else if (ix == ARR) return("arr");
	else if (ix == DSN) return("dsn");

	return("");
};

/******************************************************************************
 class QryPlnrAriList::StatApp
 ******************************************************************************/

QryPlnrAriList::StatApp::StatApp(
			const uint firstcol
			, const uint jnumFirstdisp
			, const uint ncol
			, const uint ndisp
		) : Block() {
	this->firstcol = firstcol;
	this->jnumFirstdisp = jnumFirstdisp;
	this->ncol = ncol;
	this->ndisp = ndisp;

	mask = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
};

bool QryPlnrAriList::StatApp::readXML(
			xmlXPathContext* docctx
			, string basexpath
			, bool addbasetag
		) {
	clear();

	bool basefound;

	if (addbasetag)
		basefound = checkUclcXPaths(docctx, basexpath, basexpath, "StatAppQryPlnrAriList");
	else
		basefound = checkXPath(docctx, basexpath);

	string itemtag = "StatitemAppQryPlnrAriList";

	if (basefound) {
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "firstcol", firstcol)) add(FIRSTCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "jnumFirstdisp", jnumFirstdisp)) add(JNUMFIRSTDISP);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ncol", ncol)) add(NCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ndisp", ndisp)) add(NDISP);
	};

	return basefound;
};

set<uint> QryPlnrAriList::StatApp::comm(
			const StatApp* comp
		) {
	set<uint> items;

	if (firstcol == comp->firstcol) insert(items, FIRSTCOL);
	if (jnumFirstdisp == comp->jnumFirstdisp) insert(items, JNUMFIRSTDISP);
	if (ncol == comp->ncol) insert(items, NCOL);
	if (ndisp == comp->ndisp) insert(items, NDISP);

	return(items);
};

set<uint> QryPlnrAriList::StatApp::diff(
			const StatApp* comp
		) {
	set<uint> commitems;
	set<uint> diffitems;

	commitems = comm(comp);

	diffitems = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
	for (auto it=commitems.begin();it!=commitems.end();it++) diffitems.erase(*it);

	return(diffitems);
};

