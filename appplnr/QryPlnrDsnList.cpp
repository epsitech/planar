/******************************************************************************
 class QryPlnrDsnList::VecVOrd
 ******************************************************************************/

uint QryPlnrDsnList::VecVOrd::getIx(
			const string& sref
		) {
	string s = StrMod::lc(sref);

	if (s.compare("typ") == 0) return TYP;
	else if (s.compare("dim") == 0) return DIM;
	else if (s.compare("ret") == 0) return RET;
	else if (s.compare("reu") == 0) return REU;
	else if (s.compare("prj") == 0) return PRJ;
	else if (s.compare("mty") == 0) return MTY;
	else if (s.compare("mod") == 0) return MOD;
	else if (s.compare("drv") == 0) return DRV;

	return(0);
};

string QryPlnrDsnList::VecVOrd::getSref(
			const uint ix
		) {
	if (ix == TYP) return("typ");
	else if (ix == DIM) return("dim");
	else if (ix == RET) return("ret");
	else if (ix == REU) return("reu");
	else if (ix == PRJ) return("prj");
	else if (ix == MTY) return("mty");
	else if (ix == MOD) return("mod");
	else if (ix == DRV) return("drv");

	return("");
};

/******************************************************************************
 class QryPlnrDsnList::StatApp
 ******************************************************************************/

QryPlnrDsnList::StatApp::StatApp(
			const uint firstcol
			, const uint jnumFirstdisp
			, const uint ncol
			, const uint ndisp
		) : Block() {
	this->firstcol = firstcol;
	this->jnumFirstdisp = jnumFirstdisp;
	this->ncol = ncol;
	this->ndisp = ndisp;

	mask = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
};

bool QryPlnrDsnList::StatApp::readXML(
			xmlXPathContext* docctx
			, string basexpath
			, bool addbasetag
		) {
	clear();

	bool basefound;

	if (addbasetag)
		basefound = checkUclcXPaths(docctx, basexpath, basexpath, "StatAppQryPlnrDsnList");
	else
		basefound = checkXPath(docctx, basexpath);

	string itemtag = "StatitemAppQryPlnrDsnList";

	if (basefound) {
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "firstcol", firstcol)) add(FIRSTCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "jnumFirstdisp", jnumFirstdisp)) add(JNUMFIRSTDISP);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ncol", ncol)) add(NCOL);
		if (extractUintAttrUclc(docctx, basexpath, itemtag, "Si", "sref", "ndisp", ndisp)) add(NDISP);
	};

	return basefound;
};

set<uint> QryPlnrDsnList::StatApp::comm(
			const StatApp* comp
		) {
	set<uint> items;

	if (firstcol == comp->firstcol) insert(items, FIRSTCOL);
	if (jnumFirstdisp == comp->jnumFirstdisp) insert(items, JNUMFIRSTDISP);
	if (ncol == comp->ncol) insert(items, NCOL);
	if (ndisp == comp->ndisp) insert(items, NDISP);

	return(items);
};

set<uint> QryPlnrDsnList::StatApp::diff(
			const StatApp* comp
		) {
	set<uint> commitems;
	set<uint> diffitems;

	commitems = comm(comp);

	diffitems = {FIRSTCOL, JNUMFIRSTDISP, NCOL, NDISP};
	for (auto it=commitems.begin();it!=commitems.end();it++) diffitems.erase(*it);

	return(diffitems);
};

